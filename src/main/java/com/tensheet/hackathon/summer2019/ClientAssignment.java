package com.tensheet.hackathon.summer2019;

public class ClientAssignment {

    private Set<Client> clients;

    private Set<AccountingAssociate> accountingAssociates;

    public Set<Client> getClients() {
        return clients;
    }

    public void setClients(Set<Client> clients) {
        this.clients = clients;
    }

    public Set<AccountingAssociate> getAccountingAssociates() {
        return accountingAssociates;
    }

    public void setAccountingAssociates(Set<AccountingAssociate> accountingAssociates) {
        this.accountingAssociates = accountingAssociates;
    }
}
